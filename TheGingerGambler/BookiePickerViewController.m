#pragma mark - View lifecycle

- (void)viewDidLoad
{
    [super viewDidLoad];
    NSManagedObjectContext* context = [(AppDelegate*)[[UIApplication sharedApplication] delegate] managedObjectContext];    
    NSFetchRequest* fetchRequest = [[NSFetchRequest alloc] init];
    NSEntityDescription* entity = [NSEntityDescription entityForName:@"Bookie" inManagedObjectContext:context];
    [fetchRequest setEntity:entity];
    NSError *error;
    NSArray* fetchedObjects = [context executeFetchRequest:fetchRequest error:&error];
    if(![context save:&error]){
        NSLog(@"Whoops, couldnÂ´t save: %@", [error localizedDescription]);
    }
    for (Bookie* bookie in fetchedObjects) {
        NSLog(@"Name : %@", bookie.name);
    }   
    bookies = fetchedObjects;
}

